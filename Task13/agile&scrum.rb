=========Scrum Method ==========
Scrum is a framework that helps teams work together. Much like a rugby team (where it gets its name) 
training for the big game, scrum encourages teams to learn through experiences, self-organize 
while working on a problem, and reflect on their wins and losses to continuously improve.

When to use scrum:
When requirements are not clearly defined. 
When the probability of changes during the development is high. 
When there is a need to test the solution. 
When the Product Owner (PO) is fully available. 
When the team has self-management skills.

