====== Agile methodology ======

Agile is a time boxed, iterative approach to software delivery that builds software
incrementally from the start of the project, instead of trying to deliver it all at once near the end.

Why do we use Agile?
    -Agile empowers people;
    -builds accountability,
    -encourages diversity of ideas, 
    -allows the early release of benefits, and promotes continuous improvement. 
    -It allows decisions to be tested and rejected early with feedback
     loops providing benefits that are not as evident in waterfall.

 
