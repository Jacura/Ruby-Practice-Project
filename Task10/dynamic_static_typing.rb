Type Checking :

Type checking is a program analysis that verifies something about the types that are used in the program.
The purpose of type checking is to guarantee type safety.
Type checking could happen at compile-time or at run-time.
There is two types of type checking:
1> Static Type Typing
2> Dynamic Type Typing

1) Static type checking: When variable data type is defined.
For example, in Java,C ,C++
int age = 20;

It is performed at compile time.
2) Dynamic type checking: When variable data type is not defined. Dynamic type checking
 results into less optimized code and runtime type error which is likely to occur 
 since it forces runtime checks every time the program executes.
For example, in Python , Ruby ,
age = 20;

It is performed at run time.

