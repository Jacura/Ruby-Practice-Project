Paperclip is intended as an easy file attachment library for ActiveRecord.
The intent behind it was to keep setup as easy as possible and to treat files
as much like other attributes as possible.

SETTING UP:
For PaperClip to work, we need to have ImageMagick installed on our machine.
Ubuntu users should run the command:
sudo apt-get install imagemagick -y

OBJECTIVE
1> Use the Paperclip gem to upload photos as attachments to models
2> Use a default image when nothing has been attached .
3> Understand image processing and resizing with Paperclip.


